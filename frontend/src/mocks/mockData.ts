export function createMockHall(state: GameState): HallOfKaizen {
  const lastMetric = state.metrics_history.at(-1)

  return {
    verdict: state.verdict,
    accumulated_profit: state.accumulated_profit,
    budget: state.budget,
    oee_avg: lastMetric?.oee ?? 0,
    lead_time_avg: lastMetric?.lead_time_avg ?? 0,
    throughput_avg: lastMetric?.delivered_cards ?? 0,
    top_kaizens: state.active_kaizens.slice(0, 3).map((kaizen, index) => ({
      kaizen,
      label: kaizen,
      before: 0.55 + index * 0.04,
      after: 0.63 + index * 0.05,
      delta: 0.08 + index * 0.01,
    })),
    sprint_mvp: {
      sprint: lastMetric?.sprint ?? 0,
      throughput_value: lastMetric?.throughput_value ?? 0,
      oee: lastMetric?.oee ?? 0,
    },
    dev_mvp: state.developers[0]?.name ?? 'Sem MVP',
    badges: state.badges,
    timeline: state.timeline.slice(-12),
  }
}
